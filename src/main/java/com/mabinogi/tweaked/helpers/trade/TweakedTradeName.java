package com.mabinogi.tweaked.helpers.trade;

public class TweakedTradeName
{
	private String career;
	private String name;

	public TweakedTradeName(String career, String name)
	{
		this.career = career;
		this.name = name;
	}

	public String getCareer()
	{
		return career;
	}

	public String getName()
	{
		return name;
	}
}
